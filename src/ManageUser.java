    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableUser.getModel();
        Connection con = ConnectionProvider.getCon();
        try {
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select * from appuser where role='cashier'");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("appuser_pk"), rs.getString("name"), rs.getString("number"), rs.getString("mail"), rs.getString("password"), rs.getString("address"), rs.getString("status")});
            }

        } catch (SQLException ex) {
            Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txtname.getText();
        String password = txtpassword.getText();
        String email = txtmail.getText();
        String number = txtmobile.getText();
        String address = txtaddress.getText();
        String status = (String) comboboxstatus.getSelectedItem();
        if (validateField("new")) {
            JOptionPane.showMessageDialog(null, "All Fields are Required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into appuser(role,name,mail,password,number,address,status) values ('cashier',?,?,?,?,?,?)");
                ps.setString(1, name);
                ps.setString(2, email);
                ps.setString(3, password);
                ps.setString(4, number);
                ps.setString(5, address);
                ps.setString(6, status);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "New User Added Succeccfuly");
                setVisible(false);
                new ManageUser().setVisible(true);

            } catch (SQLException ex) {
                Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
        // TODO add your handling code here:
        int index = tableUser.getSelectedRow();
        TableModel model = tableUser.getModel();
        String id = model.getValueAt(index, 0).toString();
        appuserPk = Integer.parseInt(id);
        String name = model.getValueAt(index, 1).toString();
        txtname.setText(name);
        String mobile = model.getValueAt(index, 2).toString();
        txtmobile.setText(mobile);
        String mail = model.getValueAt(index, 3).toString();
        txtmail.setText(mail);
        String password = model.getValueAt(index, 4).toString();
        txtpassword.setText(password);
        String address = model.getValueAt(index, 5).toString();
        txtaddress.setText(address);
        String status = model.getValueAt(index, 6).toString();
        comboboxstatus.removeAllItems();
        if (status.equals("Active")) {
            comboboxstatus.addItem("Active");
            comboboxstatus.addItem("Inactive");
        } else {
            comboboxstatus.addItem("Inactive");
            comboboxstatus.addItem("Active");
        }
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableUserMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String name = txtname.getText();
        String password = txtpassword.getText();
        String email = txtmail.getText();
        String number = txtmobile.getText();
        String address = txtaddress.getText();
        String status = (String) comboboxstatus.getSelectedItem();
        if (validateField("edit")) {
            JOptionPane.showMessageDialog(null, "All Fields are Required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update appuser set name=?,mail=?,password=?,number=?,address=?,status=? where appuser_pk=?");
                ps.setString(1, name);
                ps.setString(2, email);
                ps.setString(3, password);
                ps.setString(4, number);
                ps.setString(5, address);
                ps.setString(6, status);
                ps.setInt(7, appuserPk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "User Updateded Successfuly");
                setVisible(false);
                new ManageUser().setVisible(true);

            } catch (SQLException ex) {
                Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

