    private boolean validateField(String formType) {
        if (formType.equals("edit") && !txtname.getText().equals("") && !txtmobile.getText().equals("") && !txtmail.getText().equals("") && !txtaddress.getText().equals("")) {
            return false;
        } else if (formType.equals("new") && !txtname.getText().equals("") && !txtmobile.getText().equals("") && !txtmail.getText().equals("") && !txtaddress.getText().equals("") && !txtpassword.getText().equals("")) {
            return false;
        } else {
            return true;
        }

    }

